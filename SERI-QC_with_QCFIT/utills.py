def pickValue(data, Name):
    index = data.find(Name)
    if index == -1:
        data = data + " \n " +Name + " " + "0"
        index = data.find(Name)
    string = data[index + len(Name): ]
    string = string.split("\n")
    a = []
    for word in string:
        try:
            a.append(float(word))
        except ValueError:
            pass

    if len(a)==0:
        if (Name != "Plane:") & (Name != "comments:"):
            return 0
        else:
            return ""
    else:
        return a[0]


def getBoundariesTable(data):
    start = data.find('JAN')
    stop = data.find('DEFAULT')
    data = data[start:stop]
    data = data.replace(':', ',')
    data = data.replace('-', ',')
    data = data.replace('/', ',')
    data = data.replace(' ', ',')
    data = data.replace(';', ',')
    data = data.replace(',,', ',')
    data = data.split('\n')[0:12]

